public class DiameterPohon {
	public static void main(String[] args){
	int pohon1 = 58;
	int pohon2 = 61;
	int pohon3 = 90;
	int pohon4 = 75;
	int pohon5 = 52;
	
	int total = pohon1 + pohon2 + pohon3 + pohon4 + pohon5;
	double mean = total / 5;
	System.out.println("Total diameter semua pohon = " + total);
	System.out.println("Rata-rata diameter semua pohon = " + mean);
	}
}