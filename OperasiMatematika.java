public class OperasiMatematika {
    public static void main(String [] args) {
        int i = 37;
        int j = 42;
        double x = 27.475;
        double y = 7.22;

        //penjumlahan angka
        System.out.println("i + j = " + (i + j));
        System.out.println("x + y = " + (x + y));

        //pengurangan angka
        System.out.println("i - j = " + (i - j));
        System.out.println("x - y = " + (x - y));

        //perkalian angka
        System.out.println("i * j = " + (i * j));
        System.out.println("x * y = " + (x * y));

        //pembagian angka
        System.out.println("i / j = " + (i / j));
        System.out.println("x / y = " + (x / y));

    }
}